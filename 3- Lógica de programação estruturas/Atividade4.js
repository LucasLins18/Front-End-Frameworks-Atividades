let nota1 = 7;
let nota2 = 9;
let media = (nota1 + nota2) / 2;

console.log("Média do aluno: " + media);

if (media >= 7) {
  console.log("Aluno aprovado!");
} else {
  console.log("Aluno reprovado!");
}